    //Add Rows
    public static void addRows(Item i)
    {
        //String.format("%.3f",i.calculatePrice()
        Object[] rowData = {i.itemId,i.name,i.getQW(),i.price,i.calculatePrice()};
        dm.addRow(rowData);
    }
    
