    private void removeByIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeByIdButtonActionPerformed
        
        try{
        int id = Integer.parseInt(idText.getText());
        
        int rowNum=-1;
        for(int i=0;i<MainForm.cor.b.productList.size();i++)
            if(id==(int)dm.getValueAt(i,0)){
               rowNum=i;
               break;
            }
        
        
        if(rowNum!=-1){
            MainForm.cor.removeItem(id);
            dm.removeRow(rowNum);
        }
        }catch(Exception e)
        {
            System.out.println("Invalid delete.Didn't enter Id !");
        }
        finally{
        idText.setText("");
        }
    }//GEN-LAST:event_removeByIdButtonActionPerformed

